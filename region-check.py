def run_automation(account, codes):
    with sync_playwright() as p:
        browser = p.chromium.launch(headless=True)
        page = browser.new_page()

        number_founded = False
        country_founded = False

        try:
            page.goto("https://www.instagram.com/accounts/password/reset/")
            page.fill("#_r_4_", account)
            page.press("#_r_4_", "Enter")

            # 로딩 대기
            page.wait_for_load_state("networkidle")

            # 디버깅용
            # html_content = page.content()
            # with open("debug_result.html", "w", encoding="utf-8") as f:
            #    f.write(html_content)

            elements = page.locator("body *").all()
            print("확인중...")
            
            for element in elements:
                    
                # 요소 내부의 텍스트 데이터 추출
                text_data = element.text_content()

                # 텍스트가 아예 없는 빈 요소는 건너뜀
                if not text_data or not text_data.strip():
                    continue
                
                text_data = text_data.strip()

                if "Get link via SMS" in text_data:
                    number_founded = True

                    for code, (country, flag) in codes:
                        if code in text_data:
                            country_founded = True
                            print(f"해당 계정은 {country} 번호로 가입되어 있습니다.{flag}")
                            break
                    
                    break
                    
        except Exception as e:
            print(f"오류 발생: {e}")

        finally:
            if not number_founded: print("전화번호를 찾지 못했습니다.")
            elif not country_founded: print("국가코드를 찾지 못했거나 알 수 없는 국가입니다.")
            browser.close()

